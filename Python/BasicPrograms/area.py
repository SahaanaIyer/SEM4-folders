#Calculate area of triangle, square and rectangle

a=int(input("Enter side of square - \n"))
print("Area of square is {}".format(a*a))

l=int(input("Enter length of rectangle -\n"))
br=int(input("Enter breadth of rectangle -\n"))
print("Area of rectangle is {}".format(l*br))

h=int(input("Enter height of triangle -\n"))
b=int(input("Enter base of triangle -\n"))
print("Area of triangle is {}".format(0.5*h*b))
